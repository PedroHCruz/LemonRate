    public boolean ListaCategorias(){
        ArrayList<String> Categorias = new ArrayList<>();
        Categorias = midiaControl.ListaCategorias();
        for(int i = 0; i <Categorias.size(); i++){
            this.comboGenero.addItem(Categorias.get(i));
        }
        return true;
    }
